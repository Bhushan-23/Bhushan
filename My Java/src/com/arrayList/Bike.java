package com.arrayList;

public class Bike {
	int mil;
	String color;
	public Bike() {
		
	}
	public Bike(int mil, String color) {
		super();
		this.mil = mil;
		this.color = color;
	}
	@Override
	public String toString() {
		return "Bike [mil=" + mil + ", color=" + color + "]";
	}
	
	

}
